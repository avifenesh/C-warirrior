def parse_ascii_map(ascii_map: str, level_id: str) -> dict:
    """Parse ASCII map and return world_config."""
    lines = [line for line in ascii_map.strip().split('\n') if line]

    spawn_x, spawn_y = None, None
    terminals = []
    tiles = []

    for y, line in enumerate(lines):
        for x, char in enumerate(line):
            if char == '@':
                spawn_x = x * 32
                spawn_y = y * 32
            elif char == 'T':
                terminals.append({"x": x * 32, "y": y * 32})
            elif char == '~':
                tiles.append({"x": x, "y": y, "type": "water"})
            elif char == 'D':
                tiles.append({"x": x, "y": y, "type": "door"})
            elif char == '*':
                tiles.append({"x": x, "y": y, "type": "tree"})
            elif char == 'O':
                tiles.append({"x": x, "y": y, "type": "rock"})
            elif char == '^':
                tiles.append({"x": x, "y": y, "type": "lava"})
            elif char == '=':
                tiles.append({"x": x, "y": y, "type": "ice"})
            elif char == '_':
                tiles.append({"x": x, "y": y, "type": "bridge"})
            elif char == ',':
                tiles.append({"x": x, "y": y, "type": "grass"})
            elif char == ':':
                tiles.append({"x": x, "y": y, "type": "path"})
            elif char == 'o':
                tiles.append({"x": x, "y": y, "type": "pit"})
            elif char == '#':
                # Interior walls only (skip border)
                is_border = (x == 0 or x == 19 or y == 0 or y == 14)
                if not is_border:
                    tiles.append({"x": x, "y": y, "type": "wall"})

    # Sort terminals top-to-bottom, left-to-right
    terminals.sort(key=lambda t: (t["y"], t["x"]))
    quest_ids = QUEST_IDS.get(level_id, [])

    for i, terminal in enumerate(terminals):
        if i < len(quest_ids):
            terminal["quest_id"] = quest_ids[i]
        else:
            terminal["quest_id"] = f"{level_id}_Q{i+1}"

    world_config = {
        "width": 20,
        "height": 15,
        "spawn_x": spawn_x,
        "spawn_y": spawn_y,
        "terminals": terminals
    }

    if tiles:
        world_config["tiles"] = tiles

    return world_config

def main():
    results = {}

    for level_id, ascii_map in LEVEL_MAPS.items():
        config = parse_ascii_map(ascii_map, level_id)
        results[level_id] = config

        print(f"\n{level_id}:")
        print(f"  spawn: ({config['spawn_x']}, {config['spawn_y']})")
        print(f"  terminals: {len(config['terminals'])}")
        for t in config['terminals']:
            print(f"    - ({t['x']}, {t['y']}) -> {t['quest_id']}")
        if 'tiles' in config:
            tile_counts = {}
            for t in config['tiles']:
                tile_counts[t['type']] = tile_counts.get(t['type'], 0) + 1
            print(f"  tiles: {tile_counts}")

    with open("/tmp/world_configs.json", "w") as f:
        json.dump(results, f, indent=2)
    print("\nSaved to /tmp/world_configs.json")

if __name__ == "__main__":
    main()
